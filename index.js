function add(a, b) {
  return a + b;
}
function subtract(a, b) {
  return a - b;
} 
function multiply(c, d) {
  return c * d;
} 
function divide(e, f) {
  return e / f;
}
function increment(e) {
  return e + 1;
}
function decrement(e) {
  return e-1;
}
function makeInt(e) {
  return parseInt (e,10);
} 
function preserveDecimal(n) {
  return parseFloat (n)
}